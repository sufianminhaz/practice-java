function calculateGrade(point){
    if( typeof point !== 'number' || point >100 ){
        return 'invalid';
    }
    if(point >= 80){
        return 'A+';
    }
    if(point >= 70 && point < 80 ){
        return 'A';
    }
    if(point >= 60 && point < 70){
        return 'A-'
    }
    if(point >= 50 && point < 60){
        return 'B'
    }
    if(point >= 40 && point < 50){
        return 'C'
    }
    if(point >= 33 && point < 40){
        return 'D'
    }
    if(point >= 0 && point < 33){
        return 'F'
    }
    
}
    
